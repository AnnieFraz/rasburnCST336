<div><strong>Are you musically inclined? Keen to get involved in the Stirling live music scene? Looking for an excuse to bring your instrument to university?</strong></div>
<p>Look no further, the Live Music Society is here to provide you with opportunities to get involved in open mics, to collaborate with fellow musicians, and to give you a place to come for equipment, instruments, and information about local music and potential gigs.</p>
<p>No matter what instrument you play, or what genres you enjoy, Live Music has a space for you, both metaphorically and physically. Our society has access to a cottage on campus where we keep our society drum kit, keyboard, and PA system, and which you can book out to use as a practice room for yourself or your future band!</p>
<p>We want to expand the society and make it as inclusive as possible, so we are very open to ideas for expansion. We hope to create sub groups for different types of musicians, and also to provide more opportunities for members to get involved in playing music in the wider Stirling community. Our aim is that our members will look to us as a social group where they can connect with likeminded people with similar interests while also putting their musical talents to good use.</p>
<p>Join our society and we will work with you to create a space for all musicians at the University of Stirling to come together and create music!</p>
